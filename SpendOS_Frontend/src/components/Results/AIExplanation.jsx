import React from 'react';
import { Sparkles } from 'lucide-react';

export default function AIExplanation({ explanation }) {
  if (!explanation) {
    return (
      <div className="text-center py-12 text-gray-500 italic">
        No comprehensive AI recommendation report is available for this analysis.
      </div>
    );
  }

  // Very basic markdown handling for bold and newlines to parse the LLM's text
  // In a real app, you might use react-markdown here
  const formattedText = explanation
    .split('\n')
    .map((paragraph, index) => {
      if (!paragraph.trim()) return null;
      
      // Handle simple **bold** syntax from Markdown
      const parts = paragraph.split(/(\*\*.*?\*\*)/g);
      const renderedParagraph = parts.map((part, i) => {
        if (part.startsWith('**') && part.endsWith('**')) {
          return <strong key={i} className="font-bold text-gray-900">{part.slice(2, -2)}</strong>;
        }
        return part;
      });

      return (
        <p key={index} className="mb-4 text-gray-700 leading-relaxed">
          {renderedParagraph}
        </p>
      );
    });

  return (
    <div className="max-w-4xl mx-auto">
      <div className="bg-gradient-to-r from-brand-600 to-indigo-700 rounded-t-xl p-6 text-white flex items-center">
        <div className="p-3 bg-white/20 rounded-xl mr-4 backdrop-blur-sm">
          <Sparkles className="w-6 h-6 text-yellow-300" />
        </div>
        <div>
          <h2 className="text-xl font-bold">Executive Intelligence Summary</h2>
          <p className="text-brand-100 text-sm mt-1">Generated by SpendOS Groq Copilot</p>
        </div>
      </div>
      
      <div className="bg-white border border-t-0 border-gray-200 rounded-b-xl shadow-sm p-8 max-w-none prose prose-brand prose-p:text-gray-600">
        {formattedText}
      </div>
    </div>
  );
}
